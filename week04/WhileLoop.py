##While Loop - Onlineexample

# Initally n= 5
n = 5
# Line 6 asks if n is greater then 5, which is True and moves on to the next line
while n > 0:
# The loop now keeps subtracting 1 from n until the output is 0
    n -= 1
# N should be 0 at the end of the loop, the loop will stop when the condition on Line 7 is FALSE
print(n)